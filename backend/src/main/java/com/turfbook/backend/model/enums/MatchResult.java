package com.turfbook.backend.model.enums;

/**
 * Match result enumeration
 * Represents the outcome of a match from a team's perspective
 */
public enum MatchResult {
    /**
     * Team won the match
     */
    WIN,

    /**
     * Team lost the match
     */
    LOSE,

    /**
     * Match ended in a draw
     */
    DRAW
}
